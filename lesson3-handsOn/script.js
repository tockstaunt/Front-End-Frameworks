let highSchoolGrade = 13;
let yourGrade;

switch(highSchoolGrade){
    case 9: yourGrade = "Freshmen";
    break;
    case 10: yourGrade = "Sophomore";
    break;
    case 11: yourGrade = "Junior";
    break;
    case 12: yourGrade = "Senior";
    break;
    default:yourGrade = "Invalid";
}
console.log(yourGrade);